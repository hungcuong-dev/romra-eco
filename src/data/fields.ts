// Deterministic shuffle: each field gets a slice of images based on its index
function getFieldImages(index: number, count = 3): string[] {
  const start = (index * 3) % fieldImages.length;
  const result: string[] = [];
  for (let i = 0; i < count; i++) {
    result.push(fieldImages[(start + i) % fieldImages.length]);
  }
  return result;
}

